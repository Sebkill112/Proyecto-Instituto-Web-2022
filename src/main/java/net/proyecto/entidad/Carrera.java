package net.proyecto.entidad;

public class Carrera {

	private int codigo, creditos, codfacultad;
	private String nomCarrera, ciclo, nomFacultad;

	public int getCodigo() {
		return codigo;
	}

	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}

	public int getCreditos() {
		return creditos;
	}

	public void setCreditos(int creditos) {
		this.creditos = creditos;
	}

	public int getCodfacultad() {
		return codfacultad;
	}

	public void setCodfacultad(int codfacultad) {
		this.codfacultad = codfacultad;
	}

	public String getNomCarrera() {
		return nomCarrera;
	}

	public void setNomCarrera(String nomCarrera) {
		this.nomCarrera = nomCarrera;
	}

	public String getCiclo() {
		return ciclo;
	}

	public void setCiclo(String ciclo) {
		this.ciclo = ciclo;
	}

	public String getNomFacultad() {
		return nomFacultad;
	}

	public void setNomFacultad(String nomFacultad) {
		this.nomFacultad = nomFacultad;
	}

}
